// prints string as hex
static void phex(uint8_t *str)
{
    uint8_t len = 16;

    unsigned char i;
    printf("\t");
    for (i = 0; i < len; ++i)
    {
        printf("%.2x ", str[i]);
    }
    printf("\n");
}

